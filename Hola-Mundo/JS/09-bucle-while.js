'use strict'

// Bucle while
// Ejecuta una sentencia mientras una condición se cumpla   y luego se ejecuta una sentencia de fin de bucle

var year = 2018;

while(year < 2051){
    console.log("Estamos en el año: " + year);
    year++;
}

while(year != 1992){
    console.log("Estamos en el año: " + year);
    year--;
}